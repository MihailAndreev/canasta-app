/**
 * Setup tab switching behavior
 */
function setupTabSwitching(contentEl) {
  const tabBtns = contentEl.querySelectorAll('.tab-btn');
  const tabPanels = contentEl.querySelectorAll('.tab-panel');

  tabBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const targetTab = btn.dataset.tab;

      // Update buttons
      tabBtns.forEach(b => b.classList.remove('is-active'));
      btn.classList.add('is-active');

      // Update panels
      tabPanels.forEach(panel => {
        panel.classList.remove('is-active');
      });

      const targetPanel = contentEl.querySelector(`#tab-${targetTab}`);
      if (targetPanel) {
        targetPanel.classList.add('is-active');
      }

      // Update URL hash
      window.location.hash = targetTab;

      // Scroll to top
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
  });
}

/**
 * Setup TOC anchor behavior (auto-switch to Rules tab)
 */
function setupTocAnchors(contentEl) {
  const tocLinks = contentEl.querySelectorAll('.toc a');

  tocLinks.forEach(link => {
    link.addEventListener('click', (e) => {
      // Switch to Rules tab if not active
      const rulesTab = contentEl.querySelector('[data-tab="rules"]');
      const rulesPanel = contentEl.querySelector('#tab-rules');

      if (rulesTab && !rulesTab.classList.contains('is-active')) {
        // Activate Rules tab
        contentEl.querySelectorAll('.tab-btn').forEach(btn =>
          btn.classList.remove('is-active')
        );
        rulesTab.classList.add('is-active');

        // Show Rules panel
        contentEl.querySelectorAll('.tab-panel').forEach(panel =>
          panel.classList.remove('is-active')
        );
        rulesPanel.classList.add('is-active');
      }

      // Default anchor behavior will handle scroll
    });
  });
}

